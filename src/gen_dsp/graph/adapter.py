"""gen-dsp adapter: bridge dsp-graph output to gen-dsp platform backends.

Generates adapter C++ code that implements gen-dsp's ``_ext.h`` wrapper
interface by delegating to the dsp-graph compiled API.  Also generates
a ``manifest.json`` compatible with ``gen_dsp.core.manifest.Manifest``.
"""

from __future__ import annotations

import json
import shutil
from pathlib import Path
from typing import TYPE_CHECKING

from gen_dsp.graph.compile import _to_pascal, compile_graph
from gen_dsp.graph.models import Buffer, Graph

if TYPE_CHECKING:
    from gen_dsp.core.manifest import Manifest

# Platform key -> (common-header macro prefix, namespace suffix)
_PLATFORM_INFO: dict[str, tuple[str, str]] = {
    "chuck": ("CHUCK_EXT_NAME", "_chugin"),
    "clap": ("CLAP_EXT_NAME", "_clap"),
    "au": ("AU_EXT_NAME", "_au"),
    "vst3": ("VST3_EXT_NAME", "_vst3"),
    "lv2": ("LV2_EXT_NAME", "_lv2"),
    "sc": ("SC_EXT_NAME", "_sc"),
    "vcvrack": ("VCVRACK_EXT_NAME", "_vcvrack"),
    "daisy": ("DAISY_EXT_NAME", "_daisy"),
    "circle": ("CIRCLE_EXT_NAME", "_circle"),
    "pd": ("PD_EXT_NAME", "_pd"),
    "max": ("MAX_EXT_NAME", "_max"),
}

SUPPORTED_PLATFORMS = set(_PLATFORM_INFO.keys())


def generate_adapter_cpp(graph: Graph, platform: str) -> str:
    """Generate the ``_ext_{platform}.cpp`` adapter source.

    This replaces the genlib-side wrapper with dsp-graph calls while
    keeping gen-dsp's platform-side code unchanged.

    Args:
        graph: Compiled DSP graph.
        platform: gen-dsp platform key (e.g. ``"chuck"``, ``"clap"``).

    Returns:
        Complete C++ source as a string.

    Raises:
        ValueError: If *platform* is not a recognised gen-dsp platform.
    """
    if platform not in _PLATFORM_INFO:
        raise ValueError(
            f"Unknown platform {platform!r}; supported: {sorted(_PLATFORM_INFO)}"
        )

    name = graph.name
    struct = _to_pascal(name) + "State"

    lines: list[str] = []
    w = lines.append

    w(f"// _ext_{platform}.cpp -- dsp-graph adapter for gen-dsp {platform} backend")
    w("// Generated by dsp-graph")
    w("")
    w(f'#include "gen_ext_common_{platform}.h"')
    w("")
    w("// Include dsp-graph compiled code")
    w(f'#include "{name}.cpp"')
    w("")
    w("typedef void GenState;")
    w("")
    w("namespace WRAPPER_NAMESPACE {")
    w("")

    # -- lifecycle
    w("GenState* wrapper_create(float sr, long bs) {")
    w("    (void)bs;")
    w(f"    return (GenState*){name}_create(sr);")
    w("}")
    w("")
    w("void wrapper_destroy(GenState* state) {")
    w(f"    {name}_destroy(({struct}*)state);")
    w("}")
    w("")
    w("void wrapper_reset(GenState* state) {")
    w(f"    {name}_reset(({struct}*)state);")
    w("}")
    w("")

    # -- perform
    w("void wrapper_perform(GenState* state, float** ins, long numins,")
    w("                     float** outs, long numouts, long n) {")
    w("    (void)numins; (void)numouts;")
    w(f"    {name}_perform(({struct}*)state, ins, outs, (int)n);")
    w("}")
    w("")

    # -- I/O counts
    w(f"int wrapper_num_inputs() {{ return {name}_num_inputs(); }}")
    w(f"int wrapper_num_outputs() {{ return {name}_num_outputs(); }}")
    w(f"int wrapper_num_params() {{ return {name}_num_params(); }}")
    w("")

    # -- param introspection
    w("const char* wrapper_param_name(GenState* state, int index) {")
    w("    (void)state;")
    w(f"    return {name}_param_name(index);")
    w("}")
    w("")
    w("const char* wrapper_param_units(GenState* state, int index) {")
    w("    (void)state; (void)index;")
    w('    return "";')
    w("}")
    w("")
    w("float wrapper_param_min(GenState* state, int index) {")
    w("    (void)state;")
    w(f"    return {name}_param_min(index);")
    w("}")
    w("")
    w("float wrapper_param_max(GenState* state, int index) {")
    w("    (void)state;")
    w(f"    return {name}_param_max(index);")
    w("}")
    w("")
    w("char wrapper_param_hasminmax(GenState* state, int index) {")
    w("    (void)state; (void)index;")
    w("    return 1;")
    w("}")
    w("")
    w("void wrapper_set_param(GenState* state, int index, float value) {")
    w(f"    {name}_set_param(({struct}*)state, index, value);")
    w("}")
    w("")
    w("float wrapper_get_param(GenState* state, int index) {")
    w(f"    return {name}_get_param(({struct}*)state, index);")
    w("}")
    w("")

    # -- buffers
    w(f"int wrapper_num_buffers() {{ return {name}_num_buffers(); }}")
    w("")
    w("const char* wrapper_buffer_name(int index) {")
    w(f"    return {name}_buffer_name(index);")
    w("}")
    w("")
    w("} // namespace WRAPPER_NAMESPACE")
    w("")

    return "\n".join(lines)


def generate_manifest_obj(graph: Graph) -> "Manifest":
    """Generate a ``Manifest`` dataclass from a DSP graph.

    Args:
        graph: The DSP graph.

    Returns:
        A gen-dsp ``Manifest`` instance.
    """
    from gen_dsp.core.manifest import Manifest, ParamInfo
    from gen_dsp.graph import __version__

    buffer_ids = [n.id for n in graph.nodes if isinstance(n, Buffer)]
    params = [
        ParamInfo(
            index=i,
            name=p.name,
            has_minmax=True,
            min=p.min,
            max=p.max,
            default=p.default,
        )
        for i, p in enumerate(graph.params)
    ]
    return Manifest(
        gen_name=graph.name,
        num_inputs=len(graph.inputs),
        num_outputs=len(graph.outputs),
        params=params,
        buffers=buffer_ids,
        source="dsp-graph",
        version=__version__,
    )


def generate_manifest(graph: Graph) -> str:
    """Generate a ``manifest.json`` compatible with gen-dsp's ``Manifest``.

    Args:
        graph: The DSP graph.

    Returns:
        JSON string.
    """
    from gen_dsp.graph import __version__

    buffer_ids = [n.id for n in graph.nodes if isinstance(n, Buffer)]
    data = {
        "gen_name": graph.name,
        "num_inputs": len(graph.inputs),
        "num_outputs": len(graph.outputs),
        "params": [
            {
                "index": i,
                "name": p.name,
                "has_minmax": True,
                "min": p.min,
                "max": p.max,
                "default": p.default,
            }
            for i, p in enumerate(graph.params)
        ],
        "buffers": buffer_ids,
        "source": "dsp-graph",
        "version": __version__,
    }
    return json.dumps(data, indent=2) + "\n"


def compile_for_gen_dsp(
    graph: Graph,
    output_dir: str | Path,
    platform: str,
) -> Path:
    """Compile a graph and generate gen-dsp adapter files.

    Writes three files to *output_dir*:

    - ``{name}.cpp`` -- compiled dsp-graph C++
    - ``_ext_{platform}.cpp`` -- gen-dsp adapter
    - ``manifest.json`` -- gen-dsp-compatible manifest

    Args:
        graph: The DSP graph.
        output_dir: Where to write files.
        platform: gen-dsp platform key.

    Returns:
        Path to the output directory.
    """
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    code = compile_graph(graph)
    (out / f"{graph.name}.cpp").write_text(code)

    adapter = generate_adapter_cpp(graph, platform)
    (out / f"_ext_{platform}.cpp").write_text(adapter)

    manifest = generate_manifest(graph)
    (out / "manifest.json").write_text(manifest)

    return out


def generate_graph_build_file(
    output_dir: Path,
    platform: str,
    lib_name: str,
    gen_name: str,
    num_inputs: int,
    num_outputs: int,
    num_params: int = 0,
    genext_version: str = "0.8.0",
    shared_cache: bool = False,
    midi_defines: str = "",
) -> Path:
    """Generate a simplified build file for a dsp-graph project.

    Unlike gen~ projects, dsp-graph projects have no genlib dependency,
    no gen/ subdirectory, and no json.c/json_builder.c sources.
    The compiled graph code is included via ``#include`` in the adapter.

    Args:
        output_dir: Project directory.
        platform: gen-dsp platform key.
        lib_name: Plugin/external name.
        gen_name: Graph name (used in compile definitions).
        num_inputs: Number of audio inputs.
        num_outputs: Number of audio outputs.
        num_params: Number of parameters.
        genext_version: gen-dsp version string.
        shared_cache: Whether to use shared FetchContent cache.
        midi_defines: Additional MIDI compile definitions.

    Returns:
        Path to the generated build file.
    """
    cmake_platforms = {
        "clap": _cmake_clap,
        "vst3": _cmake_vst3,
        "au": _cmake_au,
        "lv2": _cmake_lv2,
        "sc": _cmake_sc,
    }
    make_platforms = {
        "pd": _makefile_pd,
        "chuck": _makefile_chuck,
        "max": _cmake_max,
    }

    generator = cmake_platforms.get(platform) or make_platforms.get(platform)
    if generator is None:
        raise ValueError(
            f"Build file generation not yet supported for platform {platform!r}"
        )

    return generator(
        output_dir=output_dir,
        lib_name=lib_name,
        gen_name=gen_name,
        num_inputs=num_inputs,
        num_outputs=num_outputs,
        num_params=num_params,
        genext_version=genext_version,
        shared_cache=shared_cache,
        midi_defines=midi_defines,
    )


def _cmake_common_header(
    lib_name: str,
    genext_version: str,
    platform: str,
    shared_cache: bool,
) -> str:
    """Generate common CMake header (project, C++ standard, cache)."""
    from gen_dsp.core.cache import get_cache_dir

    cache_dir = str(get_cache_dir())
    use_cache = "TRUE" if shared_cache else "FALSE"

    return f"""\
cmake_minimum_required(VERSION 3.19)

# {platform.upper()} plugin for {lib_name}
# Generated by gen-dsp (dsp-graph source)

set(PROJECT_NAME {lib_name})
project(${{PROJECT_NAME}})

set(CMAKE_CXX_STANDARD 20)
set(CMAKE_CXX_STANDARD_REQUIRED ON)

if(DEFINED ENV{{GEN_DSP_CACHE_DIR}})
    file(TO_CMAKE_PATH "$ENV{{GEN_DSP_CACHE_DIR}}" _gen_dsp_cache)
    set(FETCHCONTENT_BASE_DIR "${{_gen_dsp_cache}}" CACHE PATH "" FORCE)
elseif({use_cache})
    set(FETCHCONTENT_BASE_DIR "{cache_dir}" CACHE PATH "" FORCE)
endif()
"""


def _cmake_clap(**kwargs: object) -> Path:
    output_dir = kwargs["output_dir"]
    assert isinstance(output_dir, Path)
    lib_name = str(kwargs["lib_name"])
    gen_name = str(kwargs["gen_name"])
    num_inputs = int(kwargs["num_inputs"])  # type: ignore[call-overload]
    num_outputs = int(kwargs["num_outputs"])  # type: ignore[call-overload]
    genext_version = str(kwargs["genext_version"])
    shared_cache = bool(kwargs["shared_cache"])
    midi_defines = str(kwargs["midi_defines"])

    content = _cmake_common_header(lib_name, genext_version, "clap", shared_cache)
    content += """
include(FetchContent)
FetchContent_Declare(
    clap
    GIT_REPOSITORY https://github.com/free-audio/clap.git
    GIT_TAG 1.2.2
)
FetchContent_MakeAvailable(clap)

include_directories("${CMAKE_CURRENT_SOURCE_DIR}")

add_library(${PROJECT_NAME} MODULE
    gen_ext_clap.cpp
    _ext_clap.cpp
)

"""
    content += f"""\
target_compile_definitions(${{PROJECT_NAME}} PRIVATE
    GENLIB_USE_FLOAT32
    GEN_EXT_VERSION="{genext_version}"
    CLAP_EXT_NAME={lib_name}
    GEN_EXPORTED_NAME={gen_name}
    GEN_EXPORTED_HEADER="{gen_name}.h"
    GEN_EXPORTED_CPP="{gen_name}.cpp"
    CLAP_NUM_INPUTS={num_inputs}
    CLAP_NUM_OUTPUTS={num_outputs}
    {midi_defines}
)

target_compile_options(${{PROJECT_NAME}} PRIVATE -Wno-unused-function -Wno-unused-variable)
target_link_libraries(${{PROJECT_NAME}} PRIVATE clap)

set_target_properties(${{PROJECT_NAME}} PROPERTIES PREFIX "")

if(APPLE)
    set_target_properties(${{PROJECT_NAME}} PROPERTIES
        BUNDLE TRUE
        BUNDLE_EXTENSION "clap"
        MACOSX_BUNDLE_GUI_IDENTIFIER "com.gen-dsp.${{PROJECT_NAME}}"
        MACOSX_BUNDLE_BUNDLE_VERSION "{genext_version}"
    )
    add_custom_command(TARGET ${{PROJECT_NAME}} POST_BUILD
        COMMAND codesign --force --sign - "$<TARGET_BUNDLE_DIR:${{PROJECT_NAME}}>"
        COMMENT "Ad-hoc signing ${{PROJECT_NAME}}.clap"
    )
    install(DIRECTORY "$<TARGET_BUNDLE_DIR:${{PROJECT_NAME}}>"
            DESTINATION "$ENV{{HOME}}/Library/Audio/Plug-Ins/CLAP")
elseif(WIN32)
    set_target_properties(${{PROJECT_NAME}} PROPERTIES SUFFIX ".clap")
    install(FILES "$<TARGET_FILE:${{PROJECT_NAME}}>"
            DESTINATION "$ENV{{COMMONPROGRAMFILES}}/CLAP")
elseif(UNIX)
    set_target_properties(${{PROJECT_NAME}} PROPERTIES SUFFIX ".clap")
    install(FILES "$<TARGET_FILE:${{PROJECT_NAME}}>"
            DESTINATION "$ENV{{HOME}}/.clap")
endif()
"""
    path = output_dir / "CMakeLists.txt"
    path.write_text(content)
    return path


