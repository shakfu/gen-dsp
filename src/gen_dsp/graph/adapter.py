def _cmake_vst3(**kwargs: object) -> Path:
    output_dir = kwargs["output_dir"]
    assert isinstance(output_dir, Path)
    lib_name = str(kwargs["lib_name"])
    gen_name = str(kwargs["gen_name"])
    num_inputs = int(kwargs["num_inputs"])  # type: ignore[call-overload]
    num_outputs = int(kwargs["num_outputs"])  # type: ignore[call-overload]
    genext_version = str(kwargs["genext_version"])
    shared_cache = bool(kwargs["shared_cache"])
    midi_defines = str(kwargs["midi_defines"])

    # Generate FUID from plugin ID (same as vst3.py)
    import hashlib

    fuid_input = f"com.gen-dsp.vst3.{lib_name}"
    md5 = hashlib.md5(fuid_input.encode()).digest()
    fuid = [f"0x{int.from_bytes(md5[i : i + 4], 'big'):08X}" for i in range(0, 16, 4)]

    content = _cmake_common_header(lib_name, genext_version, "vst3", shared_cache)
    content += f"""
set(SMTG_ENABLE_VST3_PLUGIN_EXAMPLES OFF CACHE BOOL "" FORCE)
set(SMTG_ENABLE_VSTGUI_SUPPORT OFF CACHE BOOL "" FORCE)
set(SMTG_ENABLE_VST3_HOSTING_EXAMPLES OFF CACHE BOOL "" FORCE)
set(SMTG_RUN_VST_VALIDATOR OFF CACHE BOOL "" FORCE)

include(FetchContent)
FetchContent_Declare(
    vst3sdk
    GIT_REPOSITORY https://github.com/steinbergmedia/vst3sdk.git
    GIT_TAG v3.7.9_build_61
    GIT_SHALLOW ON
)
FetchContent_MakeAvailable(vst3sdk)

include_directories("${{CMAKE_CURRENT_SOURCE_DIR}}")

smtg_add_vst3plugin(${{PROJECT_NAME}}
    gen_ext_vst3.cpp
    _ext_vst3.cpp
    ${{vst3sdk_SOURCE_DIR}}/public.sdk/source/vst/vstsinglecomponenteffect.cpp
)

target_compile_definitions(${{PROJECT_NAME}} PRIVATE
    GENLIB_USE_FLOAT32
    GEN_EXT_VERSION="{genext_version}"
    VST3_EXT_NAME={lib_name}
    GEN_EXPORTED_NAME={gen_name}
    GEN_EXPORTED_HEADER="{gen_name}.h"
    GEN_EXPORTED_CPP="{gen_name}.cpp"
    VST3_NUM_INPUTS={num_inputs}
    VST3_NUM_OUTPUTS={num_outputs}
    VST3_FUID_0={fuid[0]}
    VST3_FUID_1={fuid[1]}
    VST3_FUID_2={fuid[2]}
    VST3_FUID_3={fuid[3]}
    {midi_defines}
)

target_compile_options(${{PROJECT_NAME}} PRIVATE -Wno-unused-function -Wno-unused-variable)
"""
    path = output_dir / "CMakeLists.txt"
    path.write_text(content)
    return path


